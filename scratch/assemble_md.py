import os

def assemble_final_content():
    # 1. 정제된 번역본 읽기
    with open('refined_translation.md', 'r', encoding='utf-8') as f:
        lines = f.readlines()

    # 2. 이미지 매핑 (섹션별)
    image_map = {
        "## ■ 서문": "![도서 표지](images/cover.png)",
        "## 1장": "![심층의 강물](images/scene_ch1.png)",
        "## 2장": "![우주적 일체성](images/scene_ch2.png)",
        "## 3장": "![신성한 빛](images/scene_ch3.png)",
        "## 4장": "![시공간의 초월](images/scene_ch4.png)",
        "## 5장": "![황금 호수의 백조](images/scene_ch5.png)",
        "## 6장": "![평정 속의 안식](images/scene_ch6.png)"
    }

    final_lines = []
    for line in lines:
        final_lines.append(line)
        for key, img_tag in image_map.items():
            if line.startswith(key):
                final_lines.append("\n" + img_tag + "\n")

    # 3. 책추남 해제 및 나비 퀘스트 추가
    commentary = """
---

# 🦋 [책추남의 해제] 미운 오리 새끼에서 백조로의 초대

안녕하세요, 책추남입니다. 여러분, 오늘 우리가 함께 만난 에머슨의 ‘대령(The Over-Soul)’은 사실 우리 삶의 게임을 완전히 바꾸는 강력한 초대장입니다.

우리는 평소에 어떻게 살고 있나요? 늘 남보다 뒤처질까 봐 불안하고, 돈과 명예라는 모이를 쫓아다니는 ‘미운 오리 새끼’의 모습으로 살고 있지는 않나요? 이것이 바로 제가 늘 말씀드리는 **‘에고(Ego)의 가짜 게임’**입니다. 하지만 에머슨이 말하는 ‘대령’에 접속하는 순간, 우리는 놀라운 사실을 발견하게 됩니다. 우리는 원래 오리가 아니라, 푸른 하늘을 날아오를 수 있는 **‘백조’**였다는 사실이죠. 

에머슨이 말한 대령은 우리 모두가 연결된 거대한 우주의 와이파이와 같습니다. 여러분이 애써 애벌레처럼 땅을 기어 다니며 노력하는 것보다, 나비로 변태하여 바람의 흐름에 몸을 맡기는 것이 훨씬 더 멀리, 더 아름답게 날 수 있는 비결입니다. 여러분 안에 이미 그 위대한 나비의 날개가 있다는 것을 믿으세요.

---

# 🌏 3대 영성 도서 입체 분석 : 대령의 현대적 재해석

에머슨의 초월주의 사상은 현대 영성 거장들의 가르침과도 완벽하게 공명합니다.

### 1. 사이토 히토리 《1퍼센트 부자의 법칙》 : 내 안의 신성을 믿는 힘
일본의 납세 왕 사이토 히토리 씨는 "못 할 것도 없지!"라고 말하는 습관이 왜 중요한지 강조합니다. 이것은 단순히 긍정적인 생각을 하라는 것이 아닙니다. 에머슨이 말한 ‘내 안의 신성(대령)’을 전적으로 믿고, 우주의 거대한 운을 내 편으로 만드는 기술입니다. 이 절대적 신뢰가 바로 히토리 씨가 말하는 '운'의 실체입니다.

### 2. 웨인 다이어 《인스피레이션》 : 영감 속에 사는 삶
웨인 다이어 박사는 ‘영감(In-Spirit)’ 속에 산다는 것이 곧 우주의 근원과 연결되는 것이라고 말합니다. 에머슨이 말한 “시간과 공간을 초월한 영혼”이 우리 몸을 통해 드러나는 상태가 바로 인스피레이션입니다. 우리가 영감 속에 있을 때, 우리는 더 이상 에고가 아닌 대령의 도구로 쓰이게 됩니다.

### 3. 에이브러햄 힉스 《머니 룰》 : 풍요를 허용하는 기술
에이브러햄은 ‘진동적 일치’를 강조합니다. 우리가 결핍에 집중하는 것은 대령의 흐름을 막는 ‘저항’입니다. 에머슨이 “당신에게 올 것은 반드시 온다”고 말한 것처럼, 풍요는 이미 흐르고 있습니다. 그 저항을 끄고 대령의 풍요로운 흐름에 나를 완전히 내맡기는 것이 풍요의 핵심 원리입니다.

---

# 🚀 [실전 나비 퀘스트] 대령의 힌트를 잡는 3단계 가이드

이제 관념을 넘어 실전으로 들어갈 시간입니다. 오늘부터 딱 세 가지만 실천해 보세요.

1. **[1단계] 동시성(Synchronicity) 일기 쓰기**: 
   오늘 하루, 우연히 만난 사람이나 우연히 들은 노래 가사 속에 숨겨진 대령의 메시지를 딱 하나만 찾아 기록해 보세요. 우주는 늘 여러분에게 말을 걸고 있습니다.

2. **[2단계] 하루 5분 '행운의 뇌파' 맞추기**: 
   아침에 눈을 뜨자마자, 혹은 잠들기 전 5분 동안 "나는 이미 충분하다"는 풍요의 느낌에 잠겨 보세요. 에고의 불안한 뇌파를 끄고 대령의 평온한 주파수에 접속하는 훈련입니다.

3. **[3단계] 에고 필터 끄기 (수용 연습)**: 
   오늘 나를 화나게 하거나 계획과 다른 일이 생길 때, "이것 또한 대령이 나를 위해 준비한 선물이다"라고 말하며 3초만 미소 지어 보세요. 가짜 게임의 필터를 끄고 진짜 게임의 흐름을 타는 연습입니다.

---
**에머슨의 지혜와 책추남의 통찰이 만나 당신의 삶이 나비처럼 날아오르길 응원합니다!**
"""
    
    with open('final_content.md', 'w', encoding='utf-8') as f:
        f.writelines(final_lines)
        f.write(commentary)

if __name__ == "__main__":
    assemble_final_content()
    print("final_content.md 생성 완료!")
